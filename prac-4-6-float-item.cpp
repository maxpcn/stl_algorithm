/*
 * =====================================================================================
 *        Class:  Item
 *  Description:  float
 * =====================================================================================
 */
class Item
{
    friend inline bool operator ==( const Item &lhs, const Item &rhs )
    {
        return ( lhs.value - rhs.value ?
                ( lhs.value - rhs.value ) / lhs.value :
                ( rhs.value - lhs.value ) / rhs.value ) > 0.000001;
    }

    public:
        /* ====================  LIFECYCLE     ======================================= */
        Item ();                             /* constructor */

        /* ====================  ACCESSORS     ======================================= */

        /* ====================  MUTATORS      ======================================= */

        /* ====================  OPERATORS     ======================================= */

    protected:
        /* ====================  METHODS       ======================================= */

        /* ====================  DATA MEMBERS  ======================================= */

    private:
        /* ====================  METHODS       ======================================= */

        /* ====================  DATA MEMBERS  ======================================= */
        float value;

}; /* -----  end of class Item  ----- */

